    private void logoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutActionPerformed
        // TODO add your handling code here:
        // Tampilkan dialog konfirmasi
        int response = JOptionPane.showConfirmDialog(null, "Yakin mau logout?", "Konfirmasi Logout",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

    // Periksa tanggapan pengguna
         if (response == JOptionPane.YES_OPTION) {
        // Jika pengguna memilih 'Yes', lakukan logout
            Login login = new Login();
            login.setVisible(true);
            this.dispose(); // Menutup jendela saat ini setelah membuka jendela login
    }
    // Jika pengguna memilih 'No', tidak lakukan apa-apa
    }//GEN-LAST:event_logoutActionPerformed

