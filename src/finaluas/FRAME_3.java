 private void hitungTotal() {
        String id = tx_idBarang.getText();
        int jumlahBarang = Integer.parseInt(jml.getText());
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;

        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/tokoSepeda", "root", "");
            String sql = "SELECT harga FROM barang WHERE id_barang = ?";
            stmt = conn.prepareStatement(sql);
            stmt.setString(1, id);
            rs = stmt.executeQuery();
            if (rs.next()) {
                int harga = rs.getInt("harga");
                int total = harga * jumlahBarang;
                tx_total.setText(String.valueOf(total));
            } else {
                JOptionPane.showMessageDialog(this, "ID Barang tidak ditemukan.");
            }
        } catch (SQLException ex) {
        } finally {
            try { if (rs != null) rs.close(); } catch (SQLException ex) { ex.printStackTrace(); }
            try { if (stmt != null) stmt.close(); } catch (SQLException ex) { ex.printStackTrace(); }
            try { if (conn != null) conn.close(); } catch (SQLException ex) { ex.printStackTrace(); }
        }
    }

 private void tambahkanTransaksi() {
        Date tanggal = new Date(tanggalbox.getDate().getTime());
        String idTransaksi = tx_idTransaksi.getText();
        String idBarang = tx_idBarang.getText();
        String idPelanggan = tx_idp.getText();
        String namaPelanggan = tx_namaPel.getText();
        String namaBarang = tx_namaBarang.getText();
        int jumlahBarang = Integer.parseInt(jml.getText());
        int totalHarga = Integer.parseInt(tx_total.getText());
        //Date tanggal = new Date(dateChooser.getDate().getTime());

        Connection conn = null;
        PreparedStatement stmt = null;

        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/tokosepeda", "root", "");
            String sql = "INSERT INTO transaksi (id_transaksi, tanggal, id_barang, id_pelanggan, nama_pelanggan, nama_barang, jumlah, total_harga ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            stmt = conn.prepareStatement(sql);
            stmt.setString(1, idTransaksi);
            stmt.setDate(2, tanggal);
            stmt.setString(3, idBarang);
            stmt.setString(4, idPelanggan);
            stmt.setString(5, namaPelanggan);
            stmt.setString(6, namaBarang);
            stmt.setInt(7, jumlahBarang);
            stmt.setInt(8, totalHarga);
            int rowsAffected = stmt.executeUpdate();
            
            String updateSql = "UPDATE barang SET jumlah_stok = jumlah_stok - ? WHERE id_barang = ?";
            stmt = conn.prepareStatement(updateSql);
            stmt.setInt(1, jumlahBarang);
            stmt.setString(2, idBarang);
            stmt.executeUpdate();

        conn.commit(); // Commit transaction

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Data transaksi berhasil ditambahkan.");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan data transaksi.");
            }
            } catch (SQLException ex) {
            ex.printStackTrace();
            refreshTable();
            id_auto();
        } finally {
            try { if (stmt != null) stmt.close(); } catch (SQLException ex) { ex.printStackTrace(); }
            try { if (conn != null) conn.close(); } catch (SQLException ex) { ex.printStackTrace(); }
        }
        
 }
        
        
        
        
