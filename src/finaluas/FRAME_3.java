     public void refreshTable(){
        //id_transaksi, tanggal, id_barang, id_pelanggan, nama_pelanggan, nama_barang, jumlah, total_harga
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Tanggal");
        model.addColumn("ID Barang");
        model.addColumn("ID Pelanggan");
        model.addColumn("Nama Pelanggan");
        model.addColumn("Nama Barang");
        model.addColumn("Jumlah");
        model.addColumn("Total Harga");
        tabel_riwayatTransaksi.setModel(model);
        
        try{
            this.stat = konek.getCon().prepareStatement("select * from transaksi");
            this.rs = stat.executeQuery();
            while (rs.next()){
                Object[] data = {
                    rs.getString("id_transaksi"),
                    rs.getDate("tanggal"),
                    rs.getString("id_barang"), 
                    rs.getString("id_pelanggan"),
                    rs.getString("nama_pelanggan"),
                    rs.getString("nama_barang"),
                    rs.getInt("jumlah"),
                    rs.getInt("total_harga")
                };
                model.addRow(data);
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
        tx_idp.setText("");
        tx_idBarang.setText("");
        tx_namaBarang.setText("");
        jml.setText("");
        tx_total.setText("");
    }
     
